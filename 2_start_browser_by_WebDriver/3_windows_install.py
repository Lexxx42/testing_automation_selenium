# Для установки откройте сайт https://sites.google.com/chromium.org/driver/
# и скачайте ту версию ChromeDriver, которая соответствует версии вашего браузера.
# Чтобы узнать версию браузера, откройте новое окно в Chrome,
# в поисковой строке наберите: chrome://version/ и нажмите Enter.
# В верхней строчке вы увидите информацию про версию браузера.

# Скачайте с сайта https://sites.google.com/chromium.org/driver/ драйвер для вашей версии браузера.
# Разархивируйте скачанный файл.
# Создайте на диске C: папку chromedriver и положите разархивированный
# ранее файл chromedriver.exe в папку C:\chromedriver.
# Добавьте в системную переменную PATH папку C:\chromedriver.
# Как это сделать в разных версиях Windows, описано здесь: https://www.computerhope.com/issues/ch000549.htm.
